public class Aritmetica {
    public double multiplicar(int n1, int n2) {
        return n1*n2;
    }
    public double dividir(int n1, int n2) {
        return n1/n2;
    }
    public double somar(int n1, int n2) {
        return n1+n2;
    }
    public double subtrair(int n1, int n2) {
        return n1-n2;
    }
    public double resto(int n1, int n2) {
        return n1%n2;
    }
}
